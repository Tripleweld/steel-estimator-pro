/**
 * Find a shape by exact name (case-insensitive).
 * @param {string} name - e.g. "W14x30"
 * @returns {object|undefined}
 */
export function findShape(name) {
  return allShapes.find(s => s.shape.toLowerCase() === name.toLowerCase());
}

/**
 * Search shapes whose name contains the query string (case-insensitive).
 * @param {string} query - e.g. "W14" or "HSS8"
 * @returns {object[]}
 */
export function searchShapes(query) {
  const q = query.toLowerCase();
  return allShapes.filter(s => s.shape.toLowerCase().includes(q));
}

